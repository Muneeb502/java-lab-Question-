
public class Modulus {

    public static void main(String[] args) {
        System.out.println("USING MODULUS FUNCTION");
        int x = 42;
        double y = 42.25;
        Double Xresult = x % y ;
        double yresult = y % x ;
        System.out.println("the result of x % y is " + Xresult );
        System.out.println("the result of y % x is " + yresult);
    }
}